-- Tabela ENDERECO
CREATE TABLE T_MT_ENDERECO (
    NR_CEP        NUMBER(15)      PRIMARY KEY,
    ID_PAIS       VARCHAR2(80),
    SG_ESTADO     VARCHAR2(2),
    ID_CIDADE     VARCHAR2(120),
    ID_BAIRRO     VARCHAR2(120),
    NR_NUMERO     NUMBER(10),
    DS_LOGRADOURO VARCHAR2(200),
    DS_COMPLEMENTO VARCHAR2(200)
);

-- Tabela MOTO
CREATE TABLE T_MT_MOTO (
    CD_PLACA   VARCHAR2(10) PRIMARY KEY,
    CD_CPF     VARCHAR2(20),
    CD_NIV     VARCHAR2(20),
    CD_MOTOR   VARCHAR2(20),
    CD_RENAVAM NUMBER(15),
    CD_FIPE    NUMBER(10)
);

-- Tabela USUARIO
CREATE TABLE T_MT_USUARIO (
    CD_CPF        VARCHAR2(20) PRIMARY KEY,
    NR_CEP        NUMBER(15) NOT NULL,
    CD_PLACA      VARCHAR2(10) NOT NULL,
    DT_NASCIMENTO DATE NOT NULL,
    ID_NOME       VARCHAR2(150) NOT NULL,
    CONSTRAINT FK_USUARIO_ENDERECO FOREIGN KEY (NR_CEP) REFERENCES T_MT_ENDERECO (NR_CEP),
    CONSTRAINT FK_USUARIO_MOTO FOREIGN KEY (CD_PLACA) REFERENCES T_MT_MOTO (CD_PLACA)
);

-- Tabela FUNCIONARIO
CREATE TABLE T_MT_FUNCIONARIO (
    ID_FUNCIONARIO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_NOME        VARCHAR2(150),
    CD_CPF         VARCHAR2(20) UNIQUE,
    CD_SENHA       VARCHAR2(120),
    NR_CEP         NUMBER(15) NOT NULL,
    CONSTRAINT FK_FUNCIONARIO_ENDERECO FOREIGN KEY (NR_CEP) REFERENCES T_MT_ENDERECO (NR_CEP)
);