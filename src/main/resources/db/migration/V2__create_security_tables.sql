CREATE TABLE T_MT_ROLE (
    ID_ROLE NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NM_ROLE VARCHAR2(50) UNIQUE NOT NULL
);

CREATE TABLE T_MT_FUNCIONARIO_ROLE (
    ID_FUNCIONARIO NUMBER NOT NULL,
    ID_ROLE NUMBER NOT NULL,
    CONSTRAINT FK_FR_FUNC FOREIGN KEY (ID_FUNCIONARIO) REFERENCES T_MT_FUNCIONARIO (ID_FUNCIONARIO),
    CONSTRAINT FK_FR_ROLE FOREIGN KEY (ID_ROLE) REFERENCES T_MT_ROLE (ID_ROLE),
    CONSTRAINT UK_FUNC_ROLE UNIQUE (ID_FUNCIONARIO, ID_ROLE)
);